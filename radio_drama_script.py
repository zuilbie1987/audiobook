async def generate_segment(index: int, role: str, text: str) -> str:
    """生成单个角色片段的音频"""
    voice = VOICES.get(role, VOICES["旁白"])
    settings = VOICE_SETTINGS.get(role, VOICE_SETTINGS["旁白"])
    
    output_file = os.path.join(OUTPUT_DIR, f"{index:04d}_{role}.mp3")
    
    communicate = edge_tts.Communicate(
        text=text,
        voice=voice,
        rate=settings["rate"],
        pitch=settings["pitch"]
    )
    await communicate.save(output_file)
    print(f"  [{index:04d}] {role}: {text[:30]}...")
    return output_file



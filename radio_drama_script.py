def merge_audio(audio_files: list, output_file: str = "射雕英雄传_第一章_风雪惊变.mp3"):
    """使用 ffmpeg 合并所有音频片段（需要安装 ffmpeg）"""
    import subprocess
    
    list_file = os.path.join(OUTPUT_DIR, "filelist.txt")
    with open(list_file, "w", encoding="utf-8") as f:
        for audio_file in audio_files:
            f.write(f"file '{os.path.abspath(audio_file)}'\n")
    
    cmd = [
        "ffmpeg", "-y",
        "-f", "concat",
        "-safe", "0",
        "-i", list_file,
        "-acodec", "libmp3lame",
        "-q:a", "2",
        output_file
    ]
    
    print(f"\n正在合并音频文件 → {output_file}")
    result = subprocess.run(cmd, capture_output=True, text=True)
    if result.returncode == 0:
        print(f"✅ 合并成功！输出文件：{output_file}")
    else:
        print(f"❌ 合并失败：{result.stderr}")
        print("请手动使用 ffmpeg 合并 audio_output/ 目录下的文件")


